#!/usr/bin/env node
import { constants as fsConstants } from 'node:fs';
import { access, mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const GENERATED_HEADER = '// @generated by scripts/generate-provider-aggregates.mjs; do not edit by hand.';
const VENDOR_DIR_PATTERN = /^[a-z][a-z0-9]*(?:-[a-z0-9]+)*$/;
const REQUIRED_LEAF_FILES = ['definition.ts', 'adapter.ts', 'provider.ts', 'index.ts'];
const GENERATED_FILES = new Map([
  ['src/provider-adapters.ts', generateProviderAdapters],
  ['src/provider-definitions.ts', generateProviderDefinitions],
  ['src/provider-factories.ts', generateProviderFactories],
]);

const packageRoot = path.resolve(fileURLToPath(import.meta.url), '..', '..');
const srcRoot = path.join(packageRoot, 'src');
const providersRoot = path.join(srcRoot, 'providers');
const generatedRoot = path.join(srcRoot, 'generated');

const args = new Set(process.argv.slice(2));
const checkMode = args.has('--check');
const listMode = args.has('--list');

if ([...args].some((arg) => !['--check', '--list'].includes(arg))) {
  console.error('Unknown argument. Supported arguments: --check, --list');
  process.exit(2);
}

try {
  const leaves = await discoverProviderLeaves();

  if (listMode) {
    for (const leaf of leaves) {
      console.log(leaf.vendor);
    }
  }

  const generated = generateAll(leaves);
  if (checkMode) {
    await checkGenerated(generated);
  } else {
    await writeGenerated(generated);
  }
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}

async function discoverProviderLeaves() {
  const entries = await readdir(providersRoot, { withFileTypes: true });
  const leaves = [];
  const aliases = new Set();

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (entry.name.startsWith('.') || entry.name === '__tests__' || entry.name === 'fixtures') {
      continue;
    }
    if (!VENDOR_DIR_PATTERN.test(entry.name)) {
      throw new Error(
        `Invalid provider leaf directory '${entry.name}'. Use lowercase ASCII kebab-case starting with a letter.`,
      );
    }

    const leafRoot = path.join(providersRoot, entry.name);
    for (const requiredFile of REQUIRED_LEAF_FILES) {
      await assertFile(path.join(leafRoot, requiredFile), entry.name);
    }

    const aliasBase = toCamelIdentifier(entry.name);
    if (aliases.has(aliasBase)) {
      throw new Error(`Provider leaf '${entry.name}' collides on generated alias '${aliasBase}'.`);
    }
    aliases.add(aliasBase);

    leaves.push({
      vendor: entry.name,
      aliasBase,
      importPath: `../providers/${entry.name}`,
    });
  }

  return leaves.sort((a, b) => (a.vendor < b.vendor ? -1 : a.vendor > b.vendor ? 1 : 0));
}

async function assertFile(filePath, vendor) {
  try {
    await access(filePath, fsConstants.R_OK);
  } catch {
    throw new Error(
      `Provider leaf '${vendor}' is missing required file '${path.basename(filePath)}'.`,
    );
  }
}

function generateAll(leaves) {
  return new Map(
    [...GENERATED_FILES.entries()].map(([fileName, generator]) => [
      fileName,
      generator(leaves),
    ]),
  );
}

function generateProviderAdapters(leaves) {
  const imports = leaves
    .map((leaf) => (
      `import { providerAdapter as ${leaf.aliasBase}ProviderAdapter } from './providers/${leaf.vendor}/adapter.js';`
    ))
    .join('\n');
  const entries = leaves
    .map((leaf) => `  ${leaf.aliasBase}ProviderAdapter,`)
    .join('\n');
  const exports = leaves
    .map((leaf) => {
      const extraExports = providerAdapterExtraExports(leaf.vendor);
      return `export { providerAdapter as ${leaf.aliasBase}Adapter${extraExports} } from './providers/${leaf.vendor}/adapter.js';`;
    })
    .join('\n');

  return withHeader(`import type { ProviderAdapterModule } from './schemas/provider-adapter.js';
${imports}

export * from './schemas/provider-adapter.js';
${exports}

export const CERTIFIED_PROVIDER_ADAPTER_MODULES = [
${entries}
] as const satisfies readonly ProviderAdapterModule[];

export type CertifiedProviderAdapterKey =
  (typeof CERTIFIED_PROVIDER_ADAPTER_MODULES)[number]['adapterKey'];
`);
}

function providerAdapterExtraExports(vendor) {
  const extrasByVendor = new Map([
    ['anthropic', ['createAnthropicAdapter']],
    ['codex-cli', ['CODEX_CLI_EXECUTION_CAPABILITY_PROFILE', 'createCodexCliAdapter', 'renderCodexCliPrompt']],
    ['ollama', ['createOllamaAdapter', 'isToolCapableModel']],
    ['openai', ['createChatCompletionsAdapter']],
    ['qwen-code', ['QWEN_CODE_EXECUTION_CAPABILITY_PROFILE', 'createQwenCodeAdapter', 'renderQwenCodePrompt']],
  ]);
  const extras = extrasByVendor.get(vendor) ?? [];
  return extras.length > 0 ? `, ${extras.join(', ')}` : '';
}

function generateProviderDefinitions(leaves) {
  const imports = leaves
    .map((leaf) => (
      `import { providerDefinition as ${leaf.aliasBase}ProviderDefinition } from './providers/${leaf.vendor}/definition.js';`
    ))
    .join('\n');
  const entries = leaves
    .map((leaf) => `  ${leaf.aliasBase}ProviderDefinition,`)
    .join('\n');

  return withHeader(`import type { ProviderDefinition, ProviderDefinitionLeaf } from './schemas/provider-definition.js';
import { hydrateProviderDefinitions } from './provider-identity.js';
${imports}

export * from './schemas/provider-definition.js';

const PROVIDER_DEFINITION_LEAVES = [
${entries}
] as const satisfies readonly ProviderDefinitionLeaf[];

export const PROVIDER_DEFINITIONS = hydrateProviderDefinitions(PROVIDER_DEFINITION_LEAVES);

export type ProviderVendorKey = (typeof PROVIDER_DEFINITION_LEAVES)[number]['vendorKey'];
export type BootstrapProviderKey = ProviderVendorKey;

export function resolveProviderDefinition(vendorKey: ProviderVendorKey): ProviderDefinition {
  const definition = PROVIDER_DEFINITIONS.find(
    (candidate) => candidate.vendorKey === vendorKey,
  );
  if (!definition) {
    throw new Error(\`Provider definition is missing for vendor key '\${vendorKey}'\`);
  }
  return definition;
}
`);
}

function generateProviderFactories(leaves) {
  const imports = leaves
    .map((leaf) => (
      `import { providerFactory as ${leaf.aliasBase}ProviderFactory } from './providers/${leaf.vendor}/provider.js';`
    ))
    .join('\n');
  const entries = leaves
    .map((leaf) => `  ${leaf.aliasBase}ProviderFactory,`)
    .join('\n');

  return withHeader(`import type { ProviderFactoryModule } from './schemas/provider-factory.js';
${imports}

export * from './schemas/provider-factory.js';

export const CERTIFIED_PROVIDER_FACTORIES = [
${entries}
] as const satisfies readonly ProviderFactoryModule[];

export type CertifiedProviderFactoryVendorKey =
  (typeof CERTIFIED_PROVIDER_FACTORIES)[number]['vendorKey'];

const PROVIDER_FACTORY_BY_VENDOR: ReadonlyMap<string, ProviderFactoryModule> = new Map(
  CERTIFIED_PROVIDER_FACTORIES.map((factory) => [
    factory.vendorKey,
    factory,
  ]),
);

export function resolveProviderFactory(vendorKey: string): ProviderFactoryModule | undefined {
  return PROVIDER_FACTORY_BY_VENDOR.get(vendorKey);
}
`);
}

function withHeader(body) {
  return `${GENERATED_HEADER}\n${body}`;
}

function toCamelIdentifier(vendor) {
  return vendor.replace(/-([a-z0-9])/g, (_, next) => next.toUpperCase());
}

async function writeGenerated(generated) {
  for (const [relativePath, contents] of generated) {
    const outputPath = path.join(packageRoot, relativePath);
    await mkdir(path.dirname(outputPath), { recursive: true });
    await writeFile(outputPath, contents, 'utf8');
  }
}

async function checkGenerated(generated) {
  const staleFiles = [];

  for (const [relativePath, expected] of generated) {
    const filePath = path.join(packageRoot, relativePath);
    let actual;
    try {
      actual = await readFile(filePath, 'utf8');
    } catch {
      staleFiles.push(toPosix(path.relative(packageRoot, filePath)));
      continue;
    }
    if (normalizeLineEndings(actual) !== expected) {
      staleFiles.push(toPosix(path.relative(packageRoot, filePath)));
    }
  }

  let generatedEntries = [];
  try {
    generatedEntries = await readdir(generatedRoot, { withFileTypes: true });
  } catch {
    generatedEntries = [];
  }

  const expectedNames = new Set(
    [...generated.keys()]
      .map((relativePath) => path.basename(relativePath))
      .filter((fileName) => fileName.endsWith('.generated.ts')),
  );
  for (const entry of generatedEntries) {
    if (entry.isFile() && entry.name.endsWith('.generated.ts') && !expectedNames.has(entry.name)) {
      staleFiles.push(toPosix(path.relative(packageRoot, path.join(generatedRoot, entry.name))));
    }
  }

  if (staleFiles.length > 0) {
    console.error('Provider generated files are stale.');
    for (const staleFile of staleFiles.sort()) {
      console.error(`- ${staleFile}`);
    }
    console.error('Run: pnpm --filter @nous/subcortex-providers run generate:providers');
    process.exit(1);
  }
}

function toPosix(value) {
  return value.split(path.sep).join('/');
}

function normalizeLineEndings(value) {
  return value.replace(/\r\n/g, '\n');
}
